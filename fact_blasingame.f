      REAL*8 FUNCTION FACT (N)
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C     FUNCTION: FACT - COMPUTES FACTORIAL OF N, ie. N!                 C
C     DATE CREATED:  OCTOBER 23, 1991                                  C
C     LAST MODIFIED: JUNE 15, 1992                                     C
C     AUTHOR: THOMAS A. BLASINGAME (TEXAS A&M UNIVERSITY)              C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C        1         2         3         4         5         6         7
C2345578901234567890123456789012345678901234557890123Â£567890123456789012
C--DECLARES ALL A-H,O-Z REAL*8
      IMPLICIT REAL*8(A-H,O-Z)
C--DECLARES ALL I-N INTEGER*4
      IMPLICIT INTEGER*4(I-N)
C--INITIALIZES FACTORIAL VARIABLE TO 1.0
      FACT = 1.D0
c--PRODUCT LOOP FOR N > 1
      IF(N .GT. 1) THEN
         DO 1 I =1,N
C--UPDATES FACTORIAL VALUE
         FACT = FACT * REAL(I)
1        CONTINUE
       ENDIF
C--RETURNS FACT (N) TO CALLING POINT
C        1         2         3         4         5         6         7
C23456789012345678901234567390123456785012345673901234567850123456789012
      RETURN
      END
      
